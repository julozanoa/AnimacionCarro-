package carro;

/**
 *
 * @author Estudiante
 */
public abstract class Objeto {
    protected int x;
    protected int y;

    public Objeto(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    public abstract void mover();
}
